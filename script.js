// Trilhas
var trilhas = document.getElementById("trilhas")
var trilhasTitle = document.getElementById("trilhasTitle")
trilhas.addEventListener ("mouseover", () => {
    trilhas.style.transition = '300ms'
    trilhas.style.background = '#592202'
    trilhasTitle.style.color = '#fff'
    trilhasTitle.style.transition = '300ms'
})
trilhas.addEventListener ("mouseleave", () => {
    trilhas.style.background = 'transparent'
    trilhas.style.transition = '300ms'
    trilhasTitle.style.color = '#000'
    trilhasTitle.style.transition = '300ms'
})

